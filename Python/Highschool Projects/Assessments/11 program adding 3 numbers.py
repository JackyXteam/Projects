# creating a list
num1 = []
# asking the user fro a number
num = input("Enter a number: ")
# appending the input
num1.append(num)
# asking again for a number
numd = input("Enter a number: ")
# appending again
num1.append(numd)
# asking again for the number
numds = input("Enter a number: ")
# appending again
num1.append(numds)
# Adding all the numbers entered
v = int(num) + int(numd) + int(numds)
# printing the equation
print(str(num1[0]) + "+" + str(num1[1]) + "+" + str(num1[2]) + "=" + str(v))

    
